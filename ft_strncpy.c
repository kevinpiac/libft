#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	int		i;

	i = 0;
	while (n--)
	{
		dst[i] = src[i];
		if (!dst[i++])
			dst[i] = '\0';
	}
	return (dst);
}
