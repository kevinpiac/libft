#include "libft.h"

char	*ft_strlcat(char *s1, const char *s2, size_t n)
{
	size_t		i;
	size_t		j;

	i = ft_strlen(s1);
	j = 0;
	while (s2[j] && i < n)
		s1[i++] = s2[j++];
	s1[i + 1] = '\0';
	return (s1);
}
